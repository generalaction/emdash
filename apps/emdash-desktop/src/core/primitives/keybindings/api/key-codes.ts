export type KeyCode = (typeof KEY_CODES)[number];

const KEY_CODE_SET: ReadonlySet<string> = new Set(KEY_CODES);

export function isKeyCode(value: string): value is KeyCode {
  return KEY_CODE_SET.has(value);
}

