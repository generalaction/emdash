export type CodeToCharMap =
  | ReadonlyMap<string, string>
  | Readonly<Partial<Record<KeyCode, string>>>;

export function codeToChar(map: CodeToCharMap, key: KeyCode): string | undefined {
  if ('get' in map && typeof map.get === 'function') {
    return map.get(key);
  }
  return (map as Readonly<Partial<Record<KeyCode, string>>>)[key];
}
